		<p><b><u>Heute:</u></b></p>
		
		<p>Wakenitz: in weiten Teilen naturnaher Flussverlauf ("Amazonas des Nordens"), in Stadtn&auml;he Seen-Charakter; Ufer gekennzeichnet durch Wiesen, Moor, Erlenbruchwald, landwirtschaftliche Nutzfl&auml;chen</p>
		
		<p>Zufl&uuml;sse in die Wakenitz: Schattiner M&uuml;hlenbach, Gr&ouml;nau, Palinger M&uuml;hlenbach, Herrenburger Landgraben, Niemarker Landgraben</p>
		
		<p>Teile der Wakenitz und der Niederungen unter Naturschutz (z.B. starke Einschr&auml;nkungen f&uuml;r das Befahren des Gew&auml;ssers mit Verbrennungsmotoren getriebenen Fahrzeugen) <a href="#1" class="hoch">1</a></p>
		
		<p>Wasserstand der Wakenitz 3,50m h&ouml;her als das Niveau der Trave, Verbindung &uuml;ber D&uuml;ker an der Falkenstra&szlig;e in den Kr&auml;henteich und weiter in die Trave (<a href="?site=allgemeinoko">mehr unter &Ouml;kosystem - allgemeine Betrachtung</a>)</p>
		
		<p>2004-2005: Bau / Erweiterung Hochwasser&uuml;berlauf am Falkendamm - Wassereinleitung in die Kanaltrave / Klughafen</p>
		
		
		
		<p><b><u>20. Jahrhundert:</u></b></p>
		
		<p>Seit 1990: Wakenitz im oberen Verlauf Grenze der Bundesl&auml;nder Schleswig-Holstein und Mecklenburg-Vorpommern</p>
		
		<p>1949-1989/1990: Wakenitz im oberen Verlauf Grenzfluss der beiden deutschen Staaten BRD und DDR</p>
		
		<p>1970: Einstellung der an der Wakenitz ans&auml;ssigen Fischerei (letzter Fischer: Heinrich Schmidt vom Fischereibetrieb Goldberg)</p>
		
		<p>systematische Vertiefung der Wakenitz durch Baggern auf 2,5m; zunehmender Motorbootverkehr</p>
		
		<p>1972:  Inbetriebnahme Grundwasserwerk Kleinensee, Abschaltung Wakenitzwasserwerk</p>
		 
		 <p> "[...] Die Brunnen [...] sind bis zu 160m tief. Eine dicke Tonschicht, [...], sch&uuml;tzt unser wichtigstes Lebensmittel vor Umwelteinfl&uuml;ssen. Wir f&uuml;gen dem frischen Wasser reinen Sauerstoff hinzu, f&uuml;hren es &uuml;ber Filter, um Eisen und Mangan zu binden sowie Tr&uuml;bungen zu entfernen, und erhalten so bestes Trinkwasser. [...]" <a href="#2" class="hoch">2</a></p>
		
		<p>1965: Aktivkohlefilteranlage zur Reduktion der Chlormengen</p>
		
		<p>Nach 1959: Zunahme der Chlormengen (St&ouml;rung biologischer Reinigungsstufe; Zunahme chlorresistenter Keime)</p>
		
		<p>1959: Bau einer chemischen Wasserreinigungsanlage</p>
		
		<p>Nach 1929: Abt&ouml;ten eingeschleppter Keime durch Reinwasserchlorung (indirekt Nachweis von Krankheitserreger &uuml;ber Nachweis von E. coli, einem im Allgemeinen unproblematisches Darmbakterium von Mensch und Tier, als Indikator f&uuml;r F&auml;kalabf&auml;lle)</p>
		
		<p>1929: Zunahme der St&ouml;rungen im Wasserwerk (Verstopfung der Filteroberfl&auml;che durch dichte Algenmatten) - Vorfilterung auf grobk&ouml;rnigem Kiesbett zur Reduktion organischer Substanz durch Schnellfilteranlage</p>
		
		<p>1925: Erweiterung des Wassereinzugsgebietes der Wakenitz auf etwa 445 km<sup>2</sup> durch Verbindung zwischen Schaalsee und Ratzeburger See (Schaalseekanal); Wassereinzug aus haupts&auml;chlich landwirtschaftlich genutzten Fl&auml;chen, aber auch Aufnahme von Abw&auml;ssern der Stadt Ratzeburg und umliegenden D&ouml;rfern sowie Eintr&auml;ge aus Nebenfl&uuml;ssen </p>
		
		<p>1900: 1. Laboratorium im Wasserwerk zur Untersuchung des Wassers nach neuen bakteriologischen Erkenntnissen (Keimzahl als Indikator f&uuml;r Wasserverunreinigung genutzt: Anzahl auf N&auml;hrbodenplatten gewachsener Bakterienkolonien nach 48 Std. bei Raumtemperatur; im Jahresmittel ca. 800 Keime/ ml, nach Regeng&uuml;ssen bis 10 000 Keime/ml  - durch Reinigung von Wakenitzwasser mittels Langsam-Sandfilter nur noch 20 Keime/ml)<a href="#3" class="hoch">3</a></p>
		
		
		
		<p><b><u>19. Jahrhundert:</u></b></p>
		
		<p>1895-1900: Bau des Elbe-L&uuml;beck-Kanals (im Altstadt-Bereich Kanaltrave genannt): Wakenitz durch Falkendamm abgetrennt, Wakenitzwasser &uuml;ber D&uuml;ker nahe der Rehderbr&uuml;cke unter der Kanaltrave in den Kr&auml;henteich geleitet <a href="#4" class="hoch">4</a> <a href="#5" class="hoch">5</a>    <a href="?site=allgemeinoko"> (<a href="?site=allgemeinoko">mehr unter &Ouml;kosystem - allgemeine Betrachtung</a>)</p>
		
		<p>1870: Vertrag mit Lauenburg: "Schonrevier" um Rothenhusen zur Sicherung des in die Wakenitz wandernden Fisches, Limitierung der Fischereibetriebe auf 11 (nicht mehr erreicht) </p>
		
		<p>1867-1900: den Anspr&uuml;chen der Zeit entsprechend ausreichende Bereitstellung von Trink- und Brauchwasser aus dem Wakenitzwasser unter Einschaltung der mechanisch und biologisch wirkenden Langsam-Sandfilter</p>
		
		<p>1867: 2 Sch&ouml;pfbrunnen pumpen  Wakenitzwasser auf 3 offene Sandfilter, anschlie&szlig;end in Reinwasserbeh&auml;lter, dann in die Stadt bzw. in einen Hochbeh&auml;lter gepumpt (Inbetriebnahme der neuen Stadtwasserkunst; Stilllegung der anderen Wasserk&uuml;nste)</p>
		
		<p>1865-1867: Bau des 1. Wasserwerkes: gusseiserne Leitungsr&ouml;hren zur Versorgung der Haushalte mit Wasser, f&uuml;r Sielsp&uuml;lungen, f&uuml;r Wasserf&uuml;hrung bei Feuergefahr, f&uuml;r die Springbrunnen auf dem Markt</p>
		
		<p>steigender Trinkwasserbedarf, Probleme mit der Sauberkeit des Wassers (Ansammlung von Keimen in den Leitungen, zunehmende Verschmutzung des Wakenitzwassers) - &Uuml;berlegungen zur Umstellung der Trinkwassergewinnung aus tiefem Grundwasser</p>
		
		<p>1832 und sp&auml;ter: Cholera-Epidemien: Forderung nach filtriertem Wasser (bislang keine wirksame Reinigung des Wassers)<a href="#6" class="hoch">6</a></p>
 
        
        <p><b><u>18. Jahrhundert:</u></b></p>
        
        <p>Anfang des 19.Jahrhunderts: Umbau einigen Fischereigeh&ouml;ften ("Horste") in Gastst&auml;tten</p>
        
        <p>1750 - 1848: regelm&auml;&szlig;ige "Wakenitzbereisung" zur Wahrung der Rechte der Stadt, zur Kontrolle des Zustandes des Fahrwassers und der Beschaffenheit der Grenzen, zur Kontrolle der Umsetzung der Fischerei-Anordnungen (z.B. von der Schilfmahd, von den Wakenitzfischern <i>"Graskrieg"</i> genannt), zur Kontrolle der Wassertiefe der Krautbeseitigung (anfangs mittels Handketscher, sp&auml;ter durch "Schlamm&uuml;hle", 1790 mittels Bagger; Vertiefung und Reinigung durch 8 Arbeiter in Sommermonaten) <a href="#7" class="hoch">7</a></p>
        
        <p>Um 1683: durch Bau von W&auml;llen und Bastionen Bildung des Stadtgrabens, Trend zum Kanal erkennbar <i>(Kanaltrave)</i>, Wakenitz als Bestandteil des mittelalterlichen &auml;u&szlig;eren Befestigungssystems der L&uuml;becker Stadtbefestigung, separate Strecke f&uuml;r Handel </p>
        
        <p>Wakenitzfischer: wohnten bis ca. 1650 innerhalb der Stadtmauer (Untere H&uuml;xstra&szlig;e, Untere Fleischhauerstra&szlig;e, An der Mauer), errichteten einfache Schutzh&uuml;tten an der Wakenitz (4 Eckpf&auml;hle, dazwischen Baumrinde und Schilf), sp&auml;ter Genehmigungen f&uuml;r Bau fester Wohngeb&auml;ude ("Horste"), kaum von Land aus erreichbar: drei "Fischerbuden", Weidmannshorst (sp&auml;ter Absalonshorst), S&uuml;verkrophorst (sp&auml;ter Hundtenhorst),  Mustinhorst (sp&auml;ter M&uuml;ggenbusch), H&uuml;xtertorhorst (sp&auml;ter Spieringshorst), Habershorst, Brunshorst, Bothenhorst, Goldberg, N&auml;dlershorst, Stoffershorst, Wakenitzhof, Ziegelhorst </p>
        
        <p>Hecht-, Schlei- und Aalfang mittels "Striek-up", "Jagelnetz", sp&auml;ter "Staaknetz" <a href="#7" class="hoch">7</a></p>
        
        
        <p><b><u>Um 1400:</u></b></p>
        
        <p>Verbreiterung der Wakenitz: urspr&uuml;nglicher Verlauf durch Aufstauungen (M&uuml;hlen- und Kr&auml;henteich) nicht mehr erkennbar</p>
		
		<p>1391-1398: Bau eines Kanals von der Trave zur Elbe (die "Stecknitzfahrt")<a href="#8" class="hoch">8</a></p>
		
		<p>Nach 1291: Aufsch&uuml;ttung des M&uuml;hlendamms - Bildung des M&uuml;hlenteiches zum Betreiben von Wasserm&uuml;hlen, sp&auml;ter weitere Aufstauungen der Wakenitz (H&uuml;xterdamm - Bildung des Kr&auml;henteiches)</p>
		
		<p>B&uuml;rgerwasserkunst vor dem H&uuml;xtertor): Hochpumpen des Trink- und Brauchwassers, Wasser &uuml;ber Holzleitungen an die Verbraucherstellen der Stadt gef&uuml;hrt (vor allem zur Bierherstellung)</p>
		
		<p>1291: Verkauf des Wakenitz-Wassers von dem Herzog von Sachsen und dem Bischof von Ratzeburg an die Stadt L&uuml;beck - gr&ouml;&szlig;ere Aufstauungen rechtlich gesichert <a href="#6" class="hoch">6</a></p>
		
		
		<p><b><u>12. Jahrhundert:</u></b></p>
		
		<p>1188: Friedrich Barbarossa genehmigt der Stadt L&uuml;beck in seinem "Privileg" das Fischen auch auf der Wakenitz, 1226 durch Friedrich II. im Reichsfreiheitsbrief best&auml;tigt </p>
		
		<p>Erste Aufstauung bereits vor 1180 (1. und 2. M&uuml;hle am M&uuml;hlentor)</p>
		
		<p>Nutzung des Wakenitzwassers zur Versorgung der Stadt: Wasserentnahme wahrscheinlich &uuml;ber Sch&ouml;pfrad in einen Beh&auml;lter, anschlie&szlig;ende Weiterleitung &uuml;ber ausgeh&ouml;lte Baumst&auml;mme (<b>Insider-Tipp nach einer Studienfahrt nach Prag 2013</b>: im alten Wasserwerk Prags an der Moldau &auml;hnliche Rohre mit Demonstration der Herstellung ausgestellt)</p>
		
		<p>L&uuml;beck Gr&uuml;ndung 1143 - starkes Wachstum der Stadt (Weg zur Hanse): Wasser aus oberfl&auml;chennahen Brunnen gewonnen (sehr hart, durch Abfallgruben der H&auml;user verunreinigt, unzureichende Mengen)</p>
		
		<p>Mitte des 12. Jahrhunderts: Erw&auml;hnung der Wakenitz in der <i>Chronica Slavorum</i> unter den Bezeichnungen <i>wochniza</i> oder <i>wochenice</i> (slawisch: Barsch-Fluss) </p>
		
		<p>Senkung der norddeutschen Tiefebene: Eindringen von Ostseewasser bis in die Trave - Schlammeintrag in den tiefen Wakenitzgraben (bis zu 6m dick) - zunehmende Vermoorung des Wakenitz-Ufers, zunehmender Bewuchs des Ufers</p>
		
		
		
		<p><b><u>Entstehung:</u></b></p>
		
		<p>Vor 115.000 bis vor ca. 11.700 Jahren: Weichsel-Eiszeit (Vergletscherung w&auml;hrend der letzten Kaltzeit im Raum Nordeuropas und im n&ouml;rdlichen Mitteleuropa)</p>
		
		<p>Formung der L&uuml;becker Bucht: gef&uuml;llt mit Schmelzwasser (Nordsee bereits weitestgehend eisfrei, Ostsee noch mehrere Eisschichten)</p>
		
		<p>R&uuml;ckzug des Eises: Bildung der Wakenitz (vor ca. 20.000 Jahren) - ein Urstromtal:</p>
		
		<p>Schmelzwasser zun&auml;chst von L&uuml;becker Bucht s&uuml;dw&auml;rts durch Ratzeburger See zur Elbe, nachdem Ostsee eisfrei - Umkehrung der Flie&szlig;richtung von S&uuml;den nach Norden zur Ostsee: deutlich gr&ouml;&szlig;eres Gef&auml;lle der Wakenitz als heute (Meerespiegel aufgrund der Eismassen am Nordpol sehr niedrig)</p>
		
		<p>Ufer wenig bewachsen und nicht vermoort</p>
		
