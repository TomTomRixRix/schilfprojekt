    <p> Dieser relativ schmale Abschnitt der Wakenitz ist ein Fliessgew&auml;sser mit geringer Str&ouml;mungsgeschwindigkeit. Der Schilfg&uuml;rtel wurde in den 70er Jahren k&uuml;nstlich (mit Hilfe von Unterwasserverbau mit Buschk&auml;sten und Tonhinterf&uuml;tterung zur Stabilisierung) durch das st&auml;dtische Wasser- und Hafenbauamt angelegt. Im S&uuml;den wird der Schilfbestand von Erlen beschattet.</p>
    
    <p><b><i>Entwicklung von 2009-2013:</i></b></p>
    <p><b>2009</b></p>
    <p>zeigte sich ein kr&auml;ftiger Halmwuchs mit guter Rispenbildung am Standort. Die Rhizomballen wurden hinter einem Unterwasserverbau auf toniger Sedimentsch&uuml;ttung aufgebracht. Das Pflanzenmaterial stammte aus dem Westuferbereich des Ratzeburger Sees. (Information von Rolf Prangel, "Wasser- und Hafenbauamt" Bauhof Weberkoppel)</p>
    <p><b>2010</b></p>
    <p>Ein gutes Wachstum mit starker Rispenbildung war zu beobachten.</p>
    <p><b>2011</b></p>
    <p>Der Schilfg&uuml;rtel zeigte im August eine gute Rispenbildung. Im Oktober gab es viele kurze Halme zwischen den langen Halmen, was auf eine neue Triebphase im Sp&auml;tsommer wies.</p>
    <p><b>2012</b></p>
    <p>An einigen Stellen ist der Schilfg&uuml;rtel etwas lichter geworden. Die Rispenbildung ist nicht mehr so ausgepr&auml;gt. Das Wasser ist sehr klar.</p>
    <p><b>2013</b></p>
    <p>Es treten L&uuml;cken im Schilfg&uuml;rtel auf, die von der Uferseite mit Wasserampfer und Seggen bewachsen werden.</p>   
