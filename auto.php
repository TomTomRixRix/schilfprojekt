            <h3 id="zonierung"><u><b>Horizontale Zonierung eines Schilfbestandes</b></u> <a href="#3" class="hoch">3</a></h3>
            
            <p>Das an einem Gew&auml;sser wachsende Schilf gliedert sich in horizontaler Richtung in drei Zonen, das Land-, das &Uuml;bergangs- und das Wasserschilf. Das Landschilf w&auml;chst landseitig oberhalb des mittleren Hochwasserpegels, das &Uuml;bergangsschilf zwischen dem mittleren Hochwasserpegel und dem mittleren Wasserpegel. Das Wasserschilf schlie&szlig;lich w&auml;chst bis oberhalb des mittleren Niedrigwasserpegels oder bis in eine Wassertiefe von etwa einem Meter bei mittlerem Wasserstand, maximal bis zu einer &Uuml;berstauungsh&ouml;he von 1,5 bis zwei Meter. <a href="#4" class="hoch">4</a></p>
            
            <p>Im Bereich des Wasserschilfs befindet sich die empfindliche wasserseitige Ausbreitungsfront des Schilfs, die sich bei g&uuml;nstigen Umweltbedingungen weiter seew&auml;rts vorschiebt, bei ung&uuml;nstigen sich wieder zur&uuml;ckzieht (siehe Tabelle unten) (Grosser et al. 1997). Die seeseitige Grenze eines Schilfbestandes liegt bei dem Sohlniveau bzw. der Wassertiefe, bei dem die Sprosse die Wasseroberfl&auml;che im Fr&uuml;hjahr noch durchsto&szlig;en k&ouml;nnen ohne f&uuml;r dieses Wachstum zu viel der in den Rhizomen gespeicherten Reservestoffe zu verbrauchen (Schmieder et al. 2003). Weiter m&uuml;ssen im ausgewachsenen Zustand des Halms drei oder vier Bl&auml;tter &uuml;ber der Wasseroberfl&auml;che vorhanden sein um Photosynthese zu betreiben und &uuml;ber das Aerenchym die Rhizome mit Sauerstoff versorgen zu k&ouml;nnen. Breitet sich das Schilf &uuml;ber diese Tiefengrenze hinaus aus, stirbt dieser Teil des Bestandes bei dem n&auml;chsten Jahr mit normalen oder h&ouml;heren Wasserst&auml;nden wieder ab, weil die Halme im Fr&uuml;hjahr die Wasseroberfl&auml;che nicht erreichen k&ouml;nnen. Gro&szlig;fl&auml;chige Schilfbest&auml;nde, an denen die horizontale Zonierung sehr gut sichtbar ist, finden sich vor allem an Flachufern von eutrophen Stillgew&auml;ssern, in denen f&uuml;r das Wachstum von Schilf gen&uuml;gend Phosphat und Stickstoff vorhanden ist. Von diesen beiden N&auml;hrstoffen werden die Vitalit&auml;t und das seew&auml;rtige Ausbreitungspotential pr&auml;gend beeinflusst (siehe Kapitel 2.3) (Rodewald-Rudescu 1974). N&auml;hrstoffarme Gew&auml;sserufer dagegen sind nur sp&auml;rlich mit Schilf bewachsen, was aber nicht bedeutet, dass diese Best&auml;nde weniger stabil sind. Ein weiterer Unterschied zu Schilfbest&auml;nden in eutrophen Gew&auml;ssern ist, dass die Zonierung in Land-, &Uuml;bergangs- und Wasserschilf nicht sehr stark sichtbar ist.</p>
            
            <p>Im &Uuml;bergangsschilf sind die h&ouml;chsten und vitalsten Halme eines Schilfbestands zu finden, die f&uuml;r das &Uuml;berleben des gesamten Bestands wichtig sind, weil sie die meisten Bl&uuml;ten bilden und auch die vegetative Vermehrung zum Teil von hier aus erfolgt (Grosser et al. 1997, Ostendorp 1993a). Das Landschilf geht mit zunehmender Entfernung vom Ufer in Weidengeb&uuml;sche oder Feuchtwiesen &uuml;ber. </p>
            
