            <p><b>Bl&uuml;tenstand: Rispe</b></p>
            
            <p>L&auml;nge: ca. 9 bis 65cm, am Ende eines Halmes</p>
            <p>besteht aus vielen gestielten &Auml;hrchen</p>
            <p>&Auml;hrchen 10 - 16 mm lang, 2 - 6-bl&uuml;tig, unterste Bl&uuml;te m&auml;nnlich, die &uuml;brigen zweigeschlechtlich</p>
            <p>im Bild: Schwellk&ouml;rper, Staubbl&auml;tter und Fruchtknoten mit Narben</p><!--Bild kommt noch-->


            <br>
            <p><b>Halm</b></p>

            <p>Halmh&ouml;he: 1,25-4,00m (abh. von Entwicklungsbedingungen und Gr&ouml;&szlig;e des jungen Unterwassersprosses im Fr&uuml;hjahr) <a href="#1" class="hoch">1</a></p>
            <p>Halmf&auml;rbung: gr&uuml;n bis gelblich-braun; bei Reife gelb; glatte, gl&auml;nzende Oberfl&auml;che</p>
            <p>Nodien: nicht herausstechende Kn&ouml;tchen am Halm</p>
            <p>Internodien: Halmabschnitte zwischen den Nodien</p>
            
            <br>
            <p><b>&Uuml;berwasserhalm</b></p>
            
            <p>Blattscheide der Bl&auml;tter um Halm gelegt: verringert Windwiderstand; Festigkeit</p>
            <p>Versorgung der Pflanze mit O<sub>2</sub> und CO<sub>2</sub> von den Bl&auml;ttern &uuml;ber Leitb&uuml;ndel bis ins Rhizom und umgekehrt</p>
            <p>Versorgung der photoynthetisierenden Pflanzenteile mit n&auml;hrsalzreichem Wasser &uuml;ber Adventivwurzeln, Rhizom und Leitb&uuml;ndel des Halmes und der Bl&auml;tter</p>
            
            <br>
            <p><b>Unterwasserhalm</b></p>
            
            <p>Von Rhizom bis Wasseroberfl&auml;che</p>
            <p>Adventivwurzeln k&ouml;nnen aus den Nodien wachsend ("Wasserbl&auml;tter"): Wasseraufnahme aufgrund des osmotischen Drucks und der Wirkung der Transpiration - mittels spezialisierter Zellen - Hydropoten ("Wassertrinker"), z.T. auch O<sub>2</sub>-Aufnahme aus dem Wasser</p>

            <br>
            <p><b>Blattquerschnitt</b></p>

            <p>Epidermis: &auml;u&szlig;ere Schutzschichten des Blattes); Schlie&szlig;zellen auf der Ober- und Unterseite (typisch f&uuml;r eine Sumpfpflanze), Zellw&auml;nde mit Silikateinlagerung (Fra&szlig;schutz)</p>
            <p>Mesophyllzellen: photosynthetisch aktiv (gr&uuml;ne L&auml;ngsstreifen des Blattes)</p>
            <p>Gelenkzellen: erm&ouml;glichen Auf- und Abrollen des Blattes; bei Wasserverlust rollt Blatt auf, bei Wasseraufnahme rollt Blatt ab</p>
            <p>Leitb&uuml;ndel (gelbe L&auml;ngsstreifen des Blattes):</p>
            <p>Holzteil (Xylem): Transport der N&auml;hrsalze in einem Wasserstrom aus Wurzeln bis in Bl&auml;tter; Wasserdampfabgabe &uuml;ber Spalt&ouml;ffnungen der Epidermen</p>
            <p>Siebteil (Phloem): Transport der in Wasser gel&ouml;sten Photosyntheseprodukte aus Bl&auml;ttern in alle Pflanzenteile &#x2192; Ziel der Blattl&auml;use</p>
Sklerenchymring um die leitenden Gewebe herum: h&auml;lt die Leitb&uuml;ndel ge&ouml;ffnet</p>
    
            <br>
            <p><b>Rhizom</b></p>
            
            <p>verzweigter, im Sediment befindlicher und Stockwerke aufbauender Sprossteil mit Luftkan&auml;len (Aerenchym)</p>
            <p>Funktion: Verankerung der Pflanze im Boden, Speicherung von N&auml;hrstoffen &rarr; Einflussfaktor f&uuml;r Wachstum (versorgen Jungsprosse im <p                >Winter unter der Eisdecke mit N&auml;hrstoffen, O<sub>2</sub> und Wasser)</p>
            <p>Im Fr&uuml;hjahr/Sommer: Abgabe des &uuml;bersch&uuml;ssigen Sauerstoffs an Umgebung, was von Bakterien zur Zersetzung organischer Stoffe genutzt wird, Aufnahme frei werdender N&auml;hrsalze durch das Rhizom</p>
            <p>Weitere Funktion: vegetative Vermehrung (siehe <a href="#fort">Fortpflanzung Schilf</a>)</p>
            <p>Lebensdauer: mehr als 3 Jahre (Absterben &auml;lterer Teile, Wachstum neuer Sprossteile)</p>

