            <h3 id="anpassung"><u><b>Anpassungen von Schilf an den Lebensraum </b></u> <a href="#2" class="hoch">2</a></h3>
            <ul>
                <li>Elastische, stabile und bruchfeste Halme (Widerstand gegen mechanische Belastungen)</li>
                <li>Tiefliegende Rhizome und Adventivwurzeln (Schutz gegen Wasserstandsschwankungen bzw. Austrocknung)</li>
                <li>Schnelles Halmwachstum (Schutz vor &Uuml;berschwemmung)</li>
                <li>Vegetative Vermehrung (Leghalme, St&auml;ngel- und Rhizomst&uuml;cke) &uuml;berwiegt gegen&uuml;ber generativer Vermehrung (Vermeidung des kritischen Keimlingsstadiums bei schwankenden Wasserstandsverh&auml;ltnissen)</li>
                <li>Ausgedehntes Aerenchym von den Blattscheiden bis in die Wurzeln (Sauerstoffversorgung im anoxischen Substrat)</li>
                <li>Sauerstoffausscheidungen in die Rhizosph&auml;re (Schutz vor phytotoxischen Substanzen)</li>
                <li>Ionenaufnahme sowohl aus dem Sediment-Porenwasser als auch aus dem &uuml;berstehenden Wasser</li>
                <li>Sekund&auml;rsprossbildung (Kompensation bei mechanischer Sch&auml;digung, bei Fra&szlig; und Sch&auml;digung durch parasitisch lebende Insekten)</li>
                <li>Dichtes Bl&auml;tterdach (Beschattung und Unterdr&uuml;ckung von Konkurrenzpflanzen)</li>
            </ul>
            
