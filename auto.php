            <h3 id="fort"><u><b>Fortpflanzung und Entwicklung</b></u></h3>
            
            <br>
            <p><b>Vegetativ &uuml;ber Rhizom (h&auml;ufigste Form):</b></p>
            
            <p>ganzj&auml;hrig &uuml;ber Rhizomausl&auml;ufer: genetisch gleiche "Klone" der Ursprungspflanze</p>
            <p>Rhizomausl&auml;ufer wachsen in der Hauptvegetationsperiode t&auml;glich bis zu 3 cm, &auml;ltere Rhizomteile sterben ab. Pflanze kann mehrere tausend Jahre alt werden.</p>
            <p>Vermehrung auch durch abgespaltene und ausgeschwemmte Rhizomteile</p>
            <p>Hauptvegatationszeit: April-Juli</p>

            
            <br>
            <p><b>Generativ &uuml;ber Samen (seltenere Form): </b></p>
            
            <p>Fr&uuml;chte: &Auml;hrchenachse mit langen, abstehenden Haaren. Ausbreitung der Frucht&auml;hrchen als Schirmchenflieger, durch Schwimm- und Wasserausbreitung oder Haftausbreitung</p>
            <p>Genotyp der Samen mit neuen genetischen Varianten im Vgl. zur Mutterpflanze</p>
            <p>Samen mehrere Monate an Wasseroberfl&auml;che treibend &uuml;berlebensf&auml;hig</p>
            <p>Festsetzen des Samens in 2-3mm tiefem Wasser</p>
            <p>Keimung nach 5-6 Tagen: Bildung eines Keimblatts (einkeimbl&auml;ttrige Pflanze)</p>
            <p>Bildung von 2-4 Prim&auml;rbl&auml;ttern: Photosynthese </p>
            <p>Wachstum: Halmh&ouml;he nach drei Monaten 20 bis 40cm, Austreiben von 4-6 Bl&auml;ttern, Bildung von Seitentrieben und Bildung des Rhizoms, Bildung von Adventivwurzeln zur Wasser- und N&auml;hrsalzaufnahme</p>
            <p>Nach 1 Jahr: Rhizom 50-70 cm lang aus 12-14 Teilen, Halm 25 bis 70 cm hoch und 3mm dick</p>
            <p>Nach 3 Jahren: erste Bl&uuml;te</p>
            <p>Nach 4 Jahren: 400 bis 1000 Halme vom etwa 8-10m breiten Rhizom gebildet</p>

            <br>
            <p><b>Wachstumsbedingungen:</b></p>
            <ul>
                <li>n&auml;hrsalzreicher Boden</li>
                <li>m&ouml;glichst kein Wellenschlag</li>
                <li>Str&ouml;mung maximal 0.2m/min, also sehr gering sein </li>
                <li>maximale Wasserh&ouml;he bei ca. 2,50 Metern</li>
                <li>ausreichende Reservestoffmengen im Rhizom</li>
                <li>gute Ausbildung der Jungsprosse </li>
            </ul>
            
