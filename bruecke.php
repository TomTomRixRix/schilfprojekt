    <p>Der Messort neben der Wallbrechtbr&uuml;cke wurde erst 2012 in das Untersuchungsprogramm aufgenommen. Durch beil&auml;ufige Beobachtungen konnte hier ein Schilfzuwachs erkannt werden, welches nun untersucht wird. Im Vergleich zu den absterbenden Schilfbest&auml;nden ist zu sehen, dass auch eine Ausbreitung von Schilfg&uuml;rteln in der Wakenitz stattfindet.</p>
    <p>Bei den Messungen 2013 konnten wir feststellen, dass insbesondere das Wasserschilf in einer Tiefe von 1 Meter noch kr&auml;ftige Schilfhalme entwickeln kann.</p>
    <p>Auch die Rispenbildung ist im Verh&auml;ltnis zu den anderen Wakenitz Standorten deutlich ausgepr&auml;gter.</p>
    <p>Schwimmblattzone und Laichkr&auml;uter sind vor der Schilfzone erkennbar. Eine  starke Entwicklung von Fadenalgen an der Oberfl&auml;che ist auch in anderen Bereichen der Wakenitz zu beobachten.</p>
    
