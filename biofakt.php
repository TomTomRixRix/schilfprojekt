	<div id="flora" name="flora">
	    <h2>Flora</h2>
	    <p>Die Wakenitz umfasst ein vielf&auml;ltiges Biotop. Vom Eulitoral bis zum Pelagial.</p>
	    <p>Im Bruchwald rund um das Flie&szlig;gew&auml;sser k&ouml;nnen Birken, Weiden, unter anderem die Lorbeerweide, Kastanien, Erlen und weitere Pflanzen vorkommen.</p>
	    <p>Das Litoral bietet einen Lebensraum f&uuml;r R&ouml;hrichtpflanzen. Unter anderem wachsen Rohrkolben, Igelkolben im Uferbereich und selbstverst&auml;ndlich das von uns erforschte Schilf, dessen Best&auml;nder&uuml;ckgang seit vielen Jahren zu beobachten ist.</p>
	    <p>Es gibt auch bl&uuml;hende Flora, wie z.B. die Schwanenblume oder der Hahnenfu&szlig;. In der Schwimmblattzone finden wir die Seerosen, die unter Naturschutz stehen. Zus&auml;tzlich gibt es auch die wei&szlig;e Seerose, welche besonderen Schutz genie&szlig;t.</p>
	    <p>Im Benthal gibt es den Laichkrautg&uuml;rtel. Dieser ist in 2-3m Tiefe vorzufinden. Innerhalb dieses G&uuml;rtels finden wir viele verschiedene Laichkraut Arten, wie das Sumpfbl&auml;ttrige Laichkraut oder die Wasserpest Elodea cadensis, welches das Problem der Verkrautung hervorruft.</p>
