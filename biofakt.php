            <h2 id="plankton"><b><u>Plankton der Wakenitz</u></b> <a href="#1" class="hoch">1</a></h2>
            
            
            <p><u>Vor Rothenhusen:</u></p>
            <p>vielf&auml;ltige, ausgewogene Planktongesellschaft, charakteristisch hier kettenbildende Diatomeen (Kieselalgen)</p>
            <p>Abnahme ihres Vorkommens (und das anderer empfindlicher Planktonarten) bis zum v&ouml;lligen Fehlen vor dem Falkendamm</p>
            
            <p><u>Im Verlauf der Wakenitz:</u></p>
            <p>im Oberlauf zun&auml;chst deutliche Abnahme des Planktons, im Unterlauf aber starke Zunahme</p>
            
            <p><b>Phytoplankton:</b></p>
            <p>Vorkommen von Blaualgen (Cyanobakterien) und Gr&uuml;nalgen sowie Massenauftreten der Kieselalge Cyclotella nehmen gleichm&auml;&szlig;ig zu (abh&auml;ngig vom stromabw&auml;rts st&auml;ndig steigenden N&auml;hrsalzgehalt des Wassers), besonders hohe Planktondichte im Kleinen See (<a href="?site=stoffkreis"> mehr unter Stoffkreisl&auml;ufe</a>)</p>
            
            
            <p><b>Zooplankton:</b></p>
            <p>weniger zahlreich als Phytoplankton, Entwicklung folgt zeitlich nach Phytoplankton</p>
            <p>Ciliaten und bestimmte R&auml;dertierchen (zum Teil von Pilzhyphen durchwachsen) am Falkendamm stark vertreten</p>
            
            <p><a href="#"> &uarr; nach oben</a></p>
