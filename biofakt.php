            <h2 id="pflanzen"><b><u>Pflanzen in und an der Wakenitz</u></b> <a href="#2" class="hoch">2</a></h2> 
            
            
            <p><b>Gew&auml;ssergrund:</b></p>
            <p>Wassermoose, Gr&uuml;nalgen, dabei auch Armleuchteralgen (nur auf Sand- und Schlickboden)</p>
            
            <p><b>Schwimmpflanzenzone</b> (2-3 m Wassertiefe):</p>
            <p>Laichkrautarten (Durchwachsenbl&auml;ttriges Laichkraut, Spiegelndes Laichkraut, Schwimmendes Laichkraut, Krauses Laichkraut) mit Toleranz gegen&uuml;ber geringer Wasserverschmutzung; weitere Wasser- und Sumpfpflanzen mit Toleranz gegen&uuml;ber Verschmutzung: Wasserstern, Wasserpest (zum Teil in Massen), Derbes Hornblatt, Wasser-Sumpfkresse, Wasserschwaden, Pfeilkraut, Einfacher Igelkolben (&uuml;ppiges Wachstum - zunehmende Verlandung) Schwimmblatt-Gew&auml;chse: Wei&szlig;e Seerose, Gelbe Teichrose (bis 2m Tiefe) - Naturschutz! Nicht im Gew&auml;sserboden verankerte Wasserpflanzen: Froschbiss, Wasserlinse, Jungpflanze der Krebsschere</p>
            
            <p><b>R&ouml;hrichtzone:</b></p>
            <p>Seebinsen - in Gesellschaft mit Mikroorganismen, die Phenole u.a. schwer abbaubare Substanzen zersetzen; unter Sauerstoffabschluss Torfbildung aus abgestorbenen Wurzel- und Rhizom-Teilen</p>
            <p>Schilf (zum Teil mit starkem Algenbewuchs), Rohrkolben, &Auml;stiger und Einfacher Igelkolben, Gro&szlig;er Hahnenfu&szlig;, Kalmus, Wasser-Schwertlilie</p>
            <p>Wasserschwaden, Wasserschierling (stark giftig), Breitbl&auml;ttriger Merk, Berle, Sumpf-Haarstrang, Engelwurz, Blutweiderich, Wasser-Sumpfkresse, Froschl&ouml;ffel, Pfeilkraut, Wasserminze, Wasserampfer u.v.m.</p>
            <p>Gro&szlig;seggen-Ried am Ufer</p>
            
            <p><b>Gef&auml;hrdete und stark gef&auml;hrdete Pflanzenarten des Wakenitz-Gebietes 1982</b> <a href="#3" class="hoch">3</a></p> 
            
            <p><u>Gew&auml;sser:</u> Froschbiss, &Auml;hriges Tausendblatt, Quirlbl&auml;ttriges Tausendblatt, Flachstengeliges Laichkraut, Stachel-Laichkraut, Stumpfbl&auml;ttriges Laichkraut, Wasserschlauch (versch. Arten)</p>
            <p><u>T&uuml;mpel:</u> Wasserfeder</p>
            <p><u>Erlenbruch:</u> Calla</p>
            <p><u>Sumpfwiese/ Sumpfmoor:</u> Schwarzkopf-Segge, Kamm-Farn, Sumpf-Farn</p>
            <p><u>Fachmoorwiese:</u> Breitbl&auml;ttriges Knabenkraut, Sumpfherzblatt, Fieberklee</p>
            
