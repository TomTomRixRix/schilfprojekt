<p id="temp"><b>Wassertemperatur und Flie&szlig;geschwindigkeit</b></p>

<p>Bei Aufheizung des Gew&auml;ssers sinkt die Sauerstoffproduktion (bei zunehmender Temperatur sinkt die L&ouml;slichkeit von O<sub>2</sub> in Wasser)</p>
<p>Wassertemperatur bei mittlerer Sauerstoffkonzentration bedeutet limitierender Faktor f&uuml;r Lebensf&auml;higkeit der Organismen</p>
<p>Wassertemperatur steigt trotz konstanter Wetterbedingungen: Indikator f&uuml;r zivilisatorische Einfl&uuml;sse (Kraftwerk, Fabrikleitung)</p>
<p>Schilfbest&auml;nde am Ufer verringern Flie&szlig;geschwindigkeit &rarr; Verlandung &rarr; neue Lebensr&auml;ume durch Schlickablagerung / f&ouml;rdern den "Selbstreinigungsprozess"</p>
<p>Algenvermehrung: F&ouml;rderung der organischen Belastung des Gew&auml;ssers </p>
<p>zu hohe Flie&szlig;geschwindigkeit behindert Schilfwachstum</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>
            
<p id="tiefe"><b>Wassertiefe, Sichttiefe, Geruch</b></p>

<p>Schilf ist an die Land-Wasser-&Uuml;bergangszone angepasst</p>
<p>Bildet geschlossenen Rohrwald bis 1,2m H&ouml;he bis 2m Tiefe</p>
<p>Tr&uuml;bung des Wassers durch Algen, organische Belastung, mineralische Bestandteile (grau durch Verunreinigungen mit Abwasser)</p>
<p>Nebeneffekt bei Tr&uuml;bungen ist Aufheizung des Wassers</p>
<p>G&uuml;te des Gew&auml;ssers l&auml;sst sich durch den Geruch erkennen (muffig deutet auf Sauerstoffarmut hin, Schaumbildung bedeutet organische Belastung)</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="bruch"><b>Bruchfestigkeit</b></p>

<p>Abnahme der Bruchfestigkeit: durch Eutrophierung der Seen und Gew&auml;sser, aber auch zunehmende mechanische Belastung (z.B. Wind, Treibholz)</p>
<p>evtl. auch genetische Ursachen verringerter Bruchfestigkeit (genetische Verarmung aufgrund vegetativer Vermehrung)</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="schlamm"><b>Schlammuntersuchungen</b></p>

<p>Schilf versorgt Boden mit Sauerstoff</p>
<p>Organische Anteile im Schlamm f&ouml;rdern anaerobe Prozesse</p>
<p>Untersuchungen durch Verbrennung des organischen Materials</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="leit"><b>Leitf&auml;higkeit</b></p>

<p>Beurteilung der Gesamtionen- oder Salzhaushaltes</p>
<p>Ladung nimmt zu, wenn Ionen vorhanden</p>
<p>Ionen transportieren Ladung</p>
<p>Erh&ouml;hte Leitf&auml;higkeit: Vermutung eines &uuml;berh&ouml;hten Gebrauchs an Winterstreuung, D&uuml;ngemittel, geologische Ver&auml;nderung, Abfall</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="haerte"><b>Wasserh&auml;rte</b></p>

<p>Angabe vom Gesamtanteil der gel&ouml;sten Calcium- und Magnesiumverbindungen, Spuren von Barium und Strontium</p>
<p>im Wasser gel&ouml;ste Verbindungen sickern durch Gesteinsschichten: regionale Unterschiede der Wasserh&auml;rte</p>
<p>weich: von 0&deg; dH bis 8,4&deg; dH</p>
<p>mittel: von 8,4&deg; dH bis 14&deg; dH</p>
<p>hart: &uuml;ber 14&deg; dH</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="pH"><b>pH-Wert</b></p>

<p>unterliegt tageszeitlichem sowie jahreszeitlichem Rhythmus</p>
<p>bestimmt das Tier- und Pflanzenvorkommen (wegen ihrer Pr&auml;ferenz- und Toleranzbereiche)</p>
<p>Skala von 0 bis 14</p>
<p>Konzentration der Hydroxoniumionen (H<sub>3</sub>O<sup>+</sup>) und Hydroxidionen (OH<sup>-</sup>): kennzeichnen sauren oder basischen Charakter der Wassers</p>
<p>normalerweise zwischen 7,5 und 8,5</p>
<p>pflanzliche Biomassenproduktion f&uuml;hrt zu Anstieg</p>
<p>sinkt der Wert, wirken Nitritionen und Phenole st&auml;rker giftig, Ammoniak wird zum ungiftigem Ammonium gebunden</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="O2"><b>Sauerstoffgehalt</b></p>

<p>Idealfall: verbrauchter O<sub>2</sub> wird durch den Austausch mit der Luft, schrittweise durch Stoffumwandlung k&ouml;rperfremder Stoffe ersetzt &rarr; hohe Sauerstoffzehrung (Bakterien bauen organisches Material zu anorganischem Material um &rarr; Mineralisation des Gew&auml;ssers)</p>
<p>&Uuml;berangebot an Nahrung f&uuml;r (erh&ouml;hte Biomasse an) Algen &rarr; Sauerstoff&uuml;bers&auml;ttigung</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="BSB5"><b>BSB<sub>5</sub>- Wert</b></p>

<p>BSB<sub>5</sub>-Wert: Summenparameter; <b>b</b>iochemischer <b>S</b>auerstoff<b>b</b>edarf in 5 Tagen</p>
<p>in der Zeit werden etwa 70% der biologisch abbaubaren Stoffe durch Bakterien abgebaut</p>
<p>mittlerer BSB<sub>5</sub>-Wert: zeigt die biologische Aktivit&auml;t des Wassers an</p>
<p>hoher BSB<sub>5</sub>-Wert: zeigt &Uuml;berma&szlig; an leicht abbaubaren Substanzen</p>
<p>niedriger BSB<sub>5</sub>-Wert: zeigt geringe Belastung des Wassers; aber auch chemische Belastung m&ouml;glich, die die Lebensf&auml;higkeit der Organismen einschr&auml;nkt</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="NO"><b>Nitrit (NO<sub>3</sub><sup>-</sup>) und Nitrat (NO<sub>2</sub><sup>-</sup>)</b></p>

<p>Nitrate sind Salze und Ester der Salpeters&auml;ure</p>
<p>wichtige Nahrungsgrundlage f&uuml;r Algen und Wasserpflanzen</p>
<p>limitierender Faktor f&uuml;r Lebensgemeinschaften</p>
<p>entsteht beim Abbau stickstoffhaltiger D&uuml;ngemittel; abgestorbene Planzenteile; G&uuml;lle; unzureichende Kl&auml;ranlagen; Humusbildung</p>
<p>sehr geringe Ammonium- und Nitritkonzentration l&auml;sst auf eine bereits durch Remineralisierung abgebaute Verschmutzung schlie&szlig;en; hoher Nitratgehalt</p>
<p>(deutsche Trinkwasserversorgung) Grenzwert: 50mg/l</p>
<p>Nitrit (krebserregend) bildet sich bei niedriger Sauerstoffkonzentration aus Nitrat (Zwischenprodukt im <a href="?site=stoffkreis#stick">Stickstoffkreislauf</a>)</p>
<p>In unbelasteten Gew&auml;ssern ist Nitrit sehr gering vorhanden</p>
<p>(deutsche Trinkwasserversorgung) Grenzwert: 0,1 mg/l</p>
<p>ab 1 mg/l ist es bedrohlich</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="NH"><b>Ammonium (NH<sub>4</sub><sup>+</sup>)</b></p>

<p>(deutsche Trinkwasserverordung) Grenzwert: 0,5 mg/l </p>
<p>hoher Wert: Gew&auml;sser verschmutzt (z.B. durch Eiwei&szlig;stoffe, D&uuml;ngerausschwemmungen, Exkremente)</p>
<p>pH-Wert basisch: Umwandlung von Ammonium zu Ammoniak (Fischgift)</p>
<p>ausreichend O<sub>2</sub>: Bakterien bauen Ammonium zu Nitrit um (giftig)</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="P"><b>Orthophosphationen</b></p>

<p>Gehalt an PO<sub>4</sub><sup>3-</sup> normalerweise sehr gering</p>
<p>(deutsche Trinkwasserverordnung) Grenzwert: 6,7 mg/l</p>
<p>h&ouml;herer Gehalt: Belastung (sehr starkes Algen- und Pflanzenwachstum; Zahl der Bakterien steigt; O<sub>2</sub>-Gehalt sinkt)</p>
<p>Schwellenwert: 0,03 mg/l (Abgrenzung der Eutrophierung eines Gew&auml;ssers)</p>
<p>ist das Wasser belastet, tritt auch Diphosphat bzw. Polyphosphat auf</p>

<p><a href="#"> &uarr; nach oben</a></p>
<br>

<p id="FeSi"><b>Eisen und Silicat</b></p>

<p>Eisen tritt in Verbindungen auf</p>
<p>Bestandteil vieler Enzyme</p>
<p>Eisenbelastung der Wassers: Tr&uuml;bung und Geschmacksver&auml;nderung</p>
<p>wird in einer Verbindung zum Reduzieren von Nitrit verwendet (wichtig f&uuml;r <a href="?site=stoffkreis#stick">Stickstoffkreislauf</a>)</p>
<p>Verwitterungsprozesse: Silicat gelangt ins Wasser</p>
<p>ist in den meisten Mineralien vorhanden</p>
<p>essentielles Mineral; beeinflusst die Stabilit&auml;t von Schilfhalmen</p>

