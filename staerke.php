	<p>Am Ende einer Vegetationsperiode werden Reservestoffe in Form von St&auml;rke in den Rhizomen des Schilfs eingelagert. Das Regenerationsverm&ouml;gen wird durch die Menge der eingelagerten St&auml;rke bestimmt. </p>
	<p>Bei einer geringen Menge an Reservestoffen reagiert der Schilfbestand sensibel auf Sch&auml;den, wobei bei einer hohen Menge an Reservestoffen keine oder nur wenige Sch&auml;den auftreten. Jungpflanzen k&ouml;nnen noch keine Fotosynthese betreiben, da sie noch nicht aus dem Wasser heraus ragen. Aus diesem Grund ist St&auml;rke f&uuml;r Jungpflanzen unbedingt notwendig, da sie ansonsten nicht wachsen k&ouml;nnen. </p>
	<p>Dazu wurden Rhizome gesammelt, die St&auml;rke darin hydrolisiert und in Glucose gemessen. Die Schilfpflanzen der Standorte Eichholz, Kleiner See und Absalonshorst zeigten deutliche Schwankungen in ihrem St&auml;rkegehalt. In Absalonshorst sind die Rhizome besonders st&auml;rkehaltig, w&auml;ren die in Eichholz eher wenig St&auml;rke beinhalten. Der St&auml;rkegehalt in den Rhizomen der Schilfpflanzen im Kleinen See und Gro&szlig; Sarau ist auch eher wenig. In Gro&szlig; Sarau h&auml;tte man mehr St&auml;rke in den Rhizomen erwartet, da sie hier am besten Wachsen.</p>
	<p>Sind hohe Anteile Ammonium im Sediment vorhanden, wird die Einlagerung von St&auml;rke im Rhizom behindert.</p>
	<p>Wenn Sauerstoffmangel herrscht und viel Stickstoff vorhanden ist, treten hohe Ammoniumwerte auf. Es folgt daraus, dass die St&auml;rkeeinlagerung bei Sauerstoffmangel und Stickstoff&uuml;berschuss verringert sein m&uuml;sste und die Pflanzen nicht optimal wachsen k&ouml;nnten.</p>
	<p>Wenn in sauerstoffreichen Sedimenten Stickstoff als Nitrat vorliegt, wurden hohe Mengen an Reservestoffen in den Rhizomen gefunden.</p>
	<p>2010 wurden in Gro&szlig; Sarau hohe Ammoniumwerte gemessen. Dies steht im Kontrast zu der Annahme, dass eine Verbindung zwischen dem Schilfsterben und der eingelagerten Menge an St&auml;rke besteht. Folgt man der Annahme, dass ein St&auml;rkemangel f&uuml;r das Schilfsterben verantwortlich ist, m&uuml;sste in Gro&szlig; Sarau wenig Ammonium gemessen werden, da hier kein Schilfsterben auftritt. </p>
	<p>Dies l&auml;sst darauf schlie&szlig;en, dass St&auml;rkemangel wahrscheinlich als Grund f&uuml;r den R&uuml;ckgang der Schilfpopulationen auszuschlie&szlig;en ist. </p>

