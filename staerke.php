        <p>Das Wachstum der Schilfhalme beginnt im Fr&uuml;hjahr an den untersuchten Standorten unterschiedlich schnell. In Gro&szlig; Sarau und Absalonshorst sind die Halme im April schon 10 cm lang, w&auml;hrend im Kleinen See und in Eichholz gerade gr&uuml;ne Spitzen aus dem Wasser ragen.  Sch&uuml;ler vermuten, dass das Wachstum durch den unterschiedlichen St&auml;rkegehalt im Rhizom beeinflusst werden k&ouml;nnte. Solange die Halme unter Wasser sind, ist Fotosynthese nur begrenzt m&ouml;glich. </p>
        <p>Um festzustellen, ob die Schilfbest&auml;nde im Fr&uuml;hjahr unterschiedliche Startbedingungen haben, soll der St&auml;rkegehalt der Rhizome gemessen werden. Dazu wurden im Januar von den vier Standorten Rhizomteile eingesammelt und im LOLA tiefgefroren. Von freiwilligen Sch&uuml;lern wurde dann in den Ferien die St&auml;rkebestimmung durchgef&uuml;hrt. Unter Anleitung von Professor Christian Schmidt wurde das Material klassifiziert, im M&ouml;rser zerrieben und im w&auml;ssrigen Milieu aufgenommen (l&ouml;sliche St&auml;rke). Um den vollst&auml;ndigen Anteil aller Energie liefernden  Kohlenhydrate zu ermitteln, wurde eine saure Hydrolyse durchgef&uuml;hrt und die Konzentration der Glukose gemessen. Dazu wurde ein Verfahren angewendet, dass kalorimetrisch die Konzentration ermitteln l&auml;sst.</p>
        <p>Bei der Auswertung der Proben mit dem Photometer zeigt sich, dass in Gro&szlig; Sarau und in Absalonshorst der St&auml;rkegehalt bei  diesen Proben deutlich gr&ouml;&szlig;er ist, als im Kleinen See und in Eichholz. </p>
        <p>Da wir nur wenige Proben untersucht haben, kann nur eine vorl&auml;ufige Aussage gemacht werden. Es m&uuml;ssten weitere Proben  bearbeitet werden.</p>


