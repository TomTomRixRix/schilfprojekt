        <h1>Gew&auml;sserg&uuml;te der Wakenitz</h1>
        
		<div class="bildContainer" style="width:45%;">
			   	
    		
    		
		</div>
		
		<p><b><u>1982</u></b></p>
		
		<p>1982 hat Manfred Diehl mit Hilfe der chemischen Gew&auml;sserg&uuml;teanalyse und der Saprobienindex die G&uuml;teklasse der Wakenitz </p>
		<p>bestimmt und kam zu dem Ergebnis, dass die  Wakenitz die G&uuml;teklasse II besitzt.</p>

		
		<p><b><u>Heute</u></b></p>
		
		<p>Im laufe unseres Schilfprojekts haben wir &uuml;ber mehrere Jahre chemische Daten zur Gew&auml;sserg&uuml;teanalyse gesammelt.</p>
		
		<p>Wir sind zu dem Schluss gekommen, dass die Wakenitz in die G&uuml;teklasse I-II einordnen l&auml;sst.</p>
		<p>Somit hat sich die Gew&auml;sserg&uuml;te der Wakenitz seit 1982 verbessert.</p>
		<p> Die genauen Werte finden Sie bei dem Unterpunkt "Ergebnisse" auf dieser Seite.</p>
