<p style="margin-top:300px">Amphibien wie Kammmolche, Rotbauchunken und Moorfr&ouml;sche sind ebenfalls in dem langsam flie&szlig;enden Gew&auml;sser  beheimatet.</p>
<p>Die gro&szlig;e Artenvielfalt um und in der Wakenitz macht es unm&ouml;glich alle tierischen Organismen auf dieser Seite darzustellen.
