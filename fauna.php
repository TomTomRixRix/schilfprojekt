<h1 style ="text-align:center";>Fauna</h1>
Die Wakenitz bietet Lebensraum f&uuml;r viele verschiedene tierische Lebensformen. Darunter gibt es zahlreiche Vogel-, Fisch-, Amphibien-, Weichtiere- und Insektenarten, die auf, neben und in der Wakenitz leben.</p>
<p>In der Wakenitz leben verbreitet Fischarten wie Hecht, Barsch, Aal, Wels und Karpfen. Vor allem Welse haben einen stabilen Bestand bilden k&ouml;nnen und werden bis zu 3 Meter lang. </p>
<img src="images/wels.jpg" alt="Wels" style="margin-left:30%">
<p>Unter anderem finden sich zahlreiche Insektenlarven und Weichtiere im Uferbereich. Beispiele hierf&uuml;r sind Libellen-, Fliegen- und M&uuml;ckenlarven und vermehrt Dreikant- und Flussperlmuscheln, sowie viele Schneckenarten. </p>
<img src="images/IMG4192.jpg" alt="Libelle" style="margin-left:30%">
<p>Die Wakenitz bietet ebenfalls Lebensraum f&uuml;r viele Vogelarten, wie Schw&auml;ne, Stockenten, Wachtelk&ouml;nige, Bl&auml;ssh&uuml;hner, Haubentaucher und Wasseramseln.</p>
