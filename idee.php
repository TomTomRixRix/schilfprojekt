<p>Der R&uuml;ckgang der Schilfbest&auml;nde ist auch bei uns an der Wakenitz insbesondere im Bereich Eichholz und am Kleinen See (im mittleren Wakenitzverlauf) an Schilfstandorten nat&uuml;rlichen Ursprungs zu beobachten. Im Bereich Absalonshorst (im oberen Wakenitzverlauf) schien ein stabiler Schilfbestand vorzuliegen, wie auch an verschiedenen Regionen des Ratzeburger Sees (beispielsweise im Bereich Gro&szlig; Sarau).</p>

<p>Im Zuge einer bereits mehrj&auml;hrigen engen Kooperation mit dem <a href="http://www.lola.uni-luebeck.de/" target="_blank">Offenen Labor der Universit&auml;t zu L&uuml;beck (LOLA)</a> und der <a href="http://tms.lernnetz.de/" target="_blank">Thomas-Mann-Schule</a> entstand die Idee, die Ursachen des beobachteten Schilfr&uuml;ckgangs in einem gemeinsamen Projekt zu untersuchen. Ein derartiges Projekt er&ouml;ffnet eine ganzheitliche Betrachtung biologischer Problemstellungen, es gestattet einen tiefen Einblick in &ouml;kologische Zusammenh&auml;nge und wissenschaftliche Arbeitsweisen: Durchf&uuml;hrung praktischer Untersuchungen, Aufstellen eigener Hypothesen f&uuml;r den Schilfr&uuml;ckgang und deren &Uuml;berpr&uuml;fung mit Hilfe universit&auml;rer M&ouml;glichkeiten. &Uuml;ber die fachliche und methodische Ebene hinaus spricht dieses Projekt eine emotionale Dimension f&uuml;r die Natur in der Umgebung an, wie es sonst kaum m&ouml;glich w&auml;re.</p>

<p>So begann dieses Projekt im Jahre 2009 mit ersten konkreten Voruntersuchungen und wurde mit gro&szlig;em Engagement f&uuml;r Oberstufenkurse, idealerweise mit naturwissenschaftlichem Profil, erarbeitet. Gef&ouml;rdert durch die Possehl-Stiftung erhalten wir  Hilfestellung durch das LOLA von der Planung &uuml;ber die Durchf&uuml;hrung bis hin zur Auswertung. Nur dadurch ist es m&ouml;glich, unseren Forschungsfragen nachgehen zu k&ouml;nnen und umfangreichere Analysen wie beispielsweise <a href="?site=genanalyse">Untersuchungen zur genetischen Variabilit&auml;t</a> des Schilfs oder <a href="?site=staerke">Untersuchungen zum St&auml;rkegehalt im Rhizom</a> mit universit&auml;ren M&ouml;glichkeiten durchzuf&uuml;hren. </p>


