	<p><b>Fragestellung:</b></p>
		<p>An welchen Stellen sind an der Wakenitz tats&auml;chlich Schilfvorkommen?</p>
		<p>Kann man die Ver&auml;nderungen der Schilfbest&auml;nde messen?</p>
	
	<p><b>L&ouml;sungsansatz:</b></p>
		<p>Vermessen und Fotografieren der Best&auml;nde von der Wasserseite in  regelm&auml;&szlig;igen Abst&auml;nden (z.B. alle 5 Jahre)</p>
	</i>
	
	<p><b>Methodisches Vorgehen</b></p>
	
	<p>Ausleihe von 4 Ruderbooten f&uuml;r je 2-3 Sch&uuml;ler und einen begleitenden Lehrer</p>
	<p>Abfahren der Wakenitz: 2 Boote an den beiden Seiten der Stadt-Wakenitz (n&ouml;rdlich der Eisenbahnbr&uuml;cke) entlang, 2 Boote die naturnahe Wakenitz s&uuml;dlich der Eisenbahnbr&uuml;cke entlang</p>

	<p>Protokollieren (Speicherung) der GPS-Koordinaten beim Passieren von Schilfbest&auml;nden (Problem: keine genauen Daten zur Breite der Schilfbest&auml;nde; keine Angaben bez&uuml;glich Wasser-, &Uuml;bergangs- oder Landschilf)</p>
	
	<p>Fotografieren des Schilfbestandes (m&ouml;glichst mittig)</p>
	
	<p>Zun&auml;chst wurden die Geokoordinaten mit einer App gemessen und die Dateien, die die aufgezeichneten Pfade enthalten, heruntergeladen. Diese konnten dann in dem Programm QGIS bearbeitet werden. Die einzelnen Koordinatenlinien, die vom Boot aus in der Mitte der Wakenitz aufgezeichnet worden waren, wurden an das Ufer des Flusses angepasst und begradigt.</p>
	<p>
	Dann wurden die Koordinaten in eine Google Map eingef&uuml;gt und konnten somit auf der Homepage angezeigt werden. Danach wurden noch andere Pflanzenbest&auml;nde vermerkt, die w&auml;hrend der Vermessung aufgenommen wurden und Fotos eingef&uuml;gt. (aufgrund ungenauer Protokollf&uuml;hrung nur Daten im Bereich Eisenbahnbr&uuml;cke bis Autobahnbr&uuml;cke verwertbar)
	</p>

