	<h2 id="phos">2. M&ouml;gliche Phosphorumsetzungen in der Wakenitz</h2>
	
	    <div class="bildContainer">
		    <a href="images/phosphatkreislauf.png" data-lightbox="phoskreis" title="Phosphorumsetzungen">	
    			<img src="images/phosphatkreislauf.png" alt=""Stickstoff"Phosphorumsetzungen">
    			<p class="bildunterschrift">Phosphorumsetzungen</p>
    		</a>
    	</div>
	<p>	Dem Phosphor kommt in Form von ATP im Energiestoffwechsel und als Baubestandteil der Nukleins&auml;uren DNA und RNA in allen Lebewesen eine zentrale Rolle zu. Er tritt in diesen Verbindungen als Phosphatester bzw. in kondensierter Form auf. </p>
    <p>In der unbelasteten Natur befinden sich Phosphate, &auml;hnlich wie Nitrate, nur in geringen Konzentrationen und beschr&auml;nken damit die Produktivit&auml;t von &Ouml;kosystemen (Minimumgesetz von Liebig). </p>
    <p>Die globale anthropogene Einbringung von Phosphat in Form von D&uuml;ngemitteln entspricht etwa dem 5-fachen Wert der nat&uuml;rlichen. Da ein Teil dieses Phosphats aus den landwirtschaftlichen B&ouml;den in nat&uuml;rliche &Ouml;kosysteme eingetragen wird, kommt es hier zu Eutrophierungserscheinungen.</p>
    
    
