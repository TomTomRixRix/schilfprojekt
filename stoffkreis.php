	<p>Einzellige und mehrzellige Organismen setzen sich aus einer Vielzahl anorganischer und organischer Stoffe zusammen, deren Zusammenspiel ihre Lebendigkeit ausmacht. Sie stehen dar&uuml;ber hinaus in einem regen Stoffaustauch mit ihrer Umgebung. Nach ihrem Tod geraten die Substanzen normalerweise in einen haupts&auml;chlich von darauf spezialisierten Organismen bewerkstelligten Kreislauf, der dazu f&uuml;hrt, dass aus der toten Biomasse von pflanzlichen Lebewesen im Prozess der Photosynthese ben&ouml;tigte N&auml;hrsalze und Kohlenstoffdioxid entstehen.</p>
	
	<p>Einen &Uuml;berblick gibt die Abbildung rechts.</p>
	
	<p>Die wichtigsten Biomolek&uuml;le aller Lebewesen sind ihre Nukleins&auml;uren, Proteine, Kohlenstoffhydrate und Lipide. Diese setzen sich haupts&auml;chlich aus den Elementen Kohlenstoff (C), Wasserstoff (H), Sauerstoff (O), Stickstoff (N), Schwefel (S) und Phosphor (P) zusammen (SCHNOP). </p>
	
	<p>Die Kreisl&auml;ufe von Stickstoff und seinen Verbindungen sowie der Kreislauf von Schwefelverbindungen spielen in der Wakenitz im Hinblick auf den Schilfr&uuml;ckgang eine besondere Rolle. Aus diesem Grund werden sie im Folgenden n&auml;her betrachtet.</p>
	
	<!--STICKSTOFF-->
	<h2 id="stick">1. Stickstoffumsetzungen in der Wakenitz</h2>
	
	    <div class="bildContainer">
		    <a href="images/stickstoffkreislauf.png" data-lightbox="stickkreis" title="Stickstoffkreislauf">	
    			<img src="images/stickstoffkreislauf.png" alt="Stickstoffkreislauf">
    			<p class="bildunterschrift">Stickstoffkreislauf</p>
    		</a>
    	</div>
	
	<p>Die wichtigsten anorganischen N-haltigen Pflanzenn&auml;hrstoffe sind Nitrat- (NO<sub>3</sub><sup>-</sup>) und Ammoniumionen (NH<sub>4</sub><sup>+</sup>). Die photosynthetisch aktiven Organismen benutzen diese N&auml;hrsalze um daraus Proteine, Proteide, Nukleins&auml;uren, ATP sowie Membranlipide aufzubauen. Im Konsumentennetz werden diese Stoffe zum Aufbau eigener Biomasse bzw. zur Energiegewinnung verwendet. Die dabei anfallenden Stoffwechselendprodukte Ammoniak (NO<sub>3</sub>), Harnstoff (N<sub>2</sub>H<sub>4</sub>CO) sowie Harns&auml;ure treten genauso wie weitere N-haltige Ausscheidungsprodukte und abgestorbene Organismen in ein Kreislaufgeschehen ein, das von Destruenten gesteuert wird und abh&auml;ngig ist von der Konzentration des Sauerstoffs im Abbaumilieu. Hohe Stickstoffeintr&auml;ge in die Wakenitz sind &uuml;ber das etwa 270 km<sup>2</sup> gro&szlig;e Wassereinzugsgebiet des Flusssystems, das zu 81 % - in der Regel konventionell, also unter hohem Minerald&uuml;nger- und Pestizideinsatz - landwirtschaftlich genutzt wird, zu verzeichnen. Auch die Luftverschmutzung leistet ihren Beitrag zur Stickstoffbelastung des Flusses. Interne Stickstoffeintr&auml;ge erfolgen &uuml;ber N-fixierende Cyanobakterien und Gr&uuml;nalgen sowie &uuml;ber die den Luftstickstoff bindenden Fadenbakterien <i>Frankia spec.</i>, die symbiontisch mit der Erle in faustgro&szlig;en, korallenartigen Wurzelknollen leben (bis zu 100 <sup>g</sup>/<sub>ha</sub> und Jahr). </p>
	
	<p>Wissenschaftliche Untersuchungen der letzten Jahre zeigen einen Zusammenhang zwischen der F&auml;higkeit der Schilfrhizome zur St&auml;rkespeicherung in Abh&auml;ngigkeit von der Ammonium- bzw. Nitratkonzentration im Substrat. Da die Menge an Reservestoffen das Regenerationsverm&ouml;gen bestimmt, k&ouml;nnte hier ein Grund f&uuml;r den Schilfr&uuml;ckgang im Untersuchungsgebiet  zu finden sein. Hohe Ammoniumwerte, die in der Regel in anoxischem Milieu auftreten, vermindern die F&auml;higkeit des Schilfrhizoms zur St&auml;rkespeicherung, hohe Nitratwerte, die mit einem relativ hohen Sauerstoffgehalt im Sediment korrelieren, beg&uuml;nstigen diese.</p>
	
	<p>Kurze Erl&auml;uterung zu den Stickstoffumwandlungsprozessen:</p>
	
	<p><b>1 = Stickstoffassimilation. </b> Hierunter versteht man den Einbau von Ammonium und Nitrat in organische stickstoffhaltige Verbindungen dieser Organismen.</p>
	
	<p><b>2 = Stickstofffixierung.</b> Der Stickstoff der Luft wird von Bakterien, Blaualgen und einigen Gr&uuml;nalgen aufgenommen, zu NH<sub>4</sub><sup>+</sup> reduziert und in dieser Oxidationsstufe in organische Verbindungen wie Aminos&auml;uren eingebaut.</p>
	
	<p><b>3 = Ammonifikation. </b>Der Abbau von Proteinen und Nukleins&auml;uren in toter Biomasse vornehmlich durch Bakterien (mikrobielle Destruenten) f&uuml;hrt zur Bildung von Ammoniumionen bzw. Ammoniak.</p>
	
	<p><b>4 = Nitrifikation. </b> Mikrobielle Umwandlung von Ammonium in Nitrit und Nitrat durch a) Nitrosomonas (Nitritbakterien, Nitritation) und b) Nitrobacter (Nitratbakterien, Nitratation)</p>
	
	<p>a. NH<sub>4</sub><sup>+</sup>  +  1 &frac12;  O<sub>2</sub>  &rarr;  NO<sub>2</sub><sup>-</sup>  +  H<sub>2</sub>O  +  2 H<sup>+</sup> &Delta;H = -319 <sup>kJ</sup>/<sub>mol</sub></p>
	<p>b. NO<sub>2</sub><sup>-</sup>  +  &frac12; O<sub>2</sub>  &rarr;  NO<sub>3</sub><sup>-</sup>    &Delta;H = -101 <sup>kJ</sup>/<sub>mol</sub></p>
	
	<p><b>5= Denitrifikation. </b> Bei der Denitrifikation wird Nitrat &uuml;ber Nitrit zu Stickstoffoxiden (NO, N<sub>2</sub>O) und Stickstoff (N<sub>2</sub>) umgewandelt (reduziert). Der Vorgang wird auch als Nitratatmung bezeichnet und l&auml;uft in anoxischem Milieu ab.</p>
	
	<p> NO<sub>3</sub><sup>-</sup> &rarr; NO<sub>2</sub><sup>-</sup> &rarr; NO &rarr; N<sub>2</sub>O &rarr; N<sub>2</sub></p>
	
	<p><b>6 = Nitratammonifikation. </b> Bei der Nitratammonifikation wird Nitrat zu Nitrit reduziert, das sich im Medium anreichert. Der Energie liefernde Elektronen-Transport endet damit. Das Nitrit wird dann auf dem Weg der so genannten Nitritreduktion zu Ammonium reduziert, das ins Medium abgegeben wird.</p>
	
	<p> NO<sub>3</sub><sup>-</sup> &rarr; NO<sub>2</sub><sup>-</sup> &rarr; NO &rarr; NH<sub>2</sub>OH &rarr; NH<sub>4</sub><sup>+</sup></p>
	
