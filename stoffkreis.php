	<h2 id="silikat">5. Silikatgehalt</h2>
	
	<p>Neben den genannten Stoffen spielt f&uuml;r das Schilfwachstum auch <b>Silikat</b> als Zellwandbestandteil eine wichtige Rolle. Dieses gilt auch f&uuml;r das Wachstum der Kieselalgen, die im Phytoplankton der Wakenitz vor allem im Fr&uuml;hjahr und Herbst die Planktonbioz&ouml;nose dominieren. </p>
	<p>Regelm&auml;&szlig;ige Bestimmungen der Silikatwerte durch Sch&uuml;ler unserer Schule zeigen, dass offensichtlich an diesem N&auml;hrsalz auch kein Mangel herrscht.</p>
	
