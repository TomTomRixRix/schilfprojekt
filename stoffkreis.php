    	</div>
	<p>In Organismen kommt Schwefel haupts&auml;chlich in reduzierter Form (Oxidationsstufe -2) in den Thiolgruppen (Sulfhydrylgruppen, -SH) von Aminos&auml;uren (Methionin, Cystein) vor und ist damit f&uuml;r den Aufbau von Proteinen unentbehrlich. Disulfidbr&uuml;cken, die durch enzymatische Wasserstoffabspaltung aus zwei Cystein-SH-Resten einer &uuml;ber Peptidbindungen verkn&uuml;pften Aminos&auml;urekette entstehen, fixieren Terti&auml;rstrukturen von Proteinen. Auch in Coenzymen, wie z.B. Acetyl-CoA und Biotin sowie in Eisen-Schwefel-Zentren von Proteinen tritt Schwefel als Elementbaustein auf. </p>
    <p>Im Folgenden werden die Abk&uuml;rzungen R-SH und R-S-R f&uuml;r S-haltige organische Verbindungen benutzt.</p>
	
	<p>Kurze Erl&auml;uterungen zu den Umwandlungsprozessen S-haltiger Stoffe:</p>
	
	<p><b>1 = assimilatorische Sulfatreduktion.</b> Die Prim&auml;rproduzenten nehmen Sulfat aus dem Wasser auf. Sulfat kommt urspr&uuml;nglich aus verschiedenen Gesteinsarten (heute auch aus mineralstoffged&uuml;ngten Feldern), aus dem es durch Wasser herausgel&ouml;st wurde und wird. Sulfat muss dann reduziert werden, damit es in organische Verbindungen eingebaut werden kann.</p>
	<p><b>2 = Desulfuration.</b>  Beim Abbau der schwefelhaltigen organischen Verbindungen durch Destruenten entsteht Schwefelwasserstoff (H<sub>2</sub>S).</p>
	<p><b>3 = Chemische Oxidation.</b> Rein chemische Oxidation von Schwefelwasserstoff zu Sulfat in oxischem Milieu.</p>
	<p><b>4 = Sulfurikation.</b> Chemoautotrophe Bakterien benutzen Schwefelwasserstoff zur Oxidation, wobei die freigesetzte Oxidationsenergie in Form von ATP und NADPH + H<sup>+</sup> konserviert werden kann.</p>
	<p><b>5 = Metallsulfidbildung.</b> Der aus der Desulfuration stammende Schwefelwasserstoff reagiert im Faulschlamm mit Metallionen zu schwer l&ouml;slichen, dunkel bis schwarz gef&auml;rbten Metallsulfiden.</p>
	<p><b>6 = Desulfurikation.</b> Sulfat wird von Desulfurikanten zu H<sub>2</sub>S reduziert. Diese nutzen den Sauerstoff im Sulfat zur Oxidation von aufgenommenen organischen Stoffen; Sulfatatmung von obligat anoxischen Bakterien.</p>
	<p><b>7 = Bakterielle Photosynthese.</b> Photoautotrophe Schwefelpurpurbakterien und gr&uuml;ne Schwefelbakterien nutzen den
Schwefelwasserstoff als H-Quelle zur Reduktion von Kohlenstoffdioxid zu Zucker. (BChl = Bakteriochlorophyll)</p>
