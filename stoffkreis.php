	    <h2><u>Stickstoffkreislauf</u></h2>
	    <p>Der Stickstoffkreislauf beschreibt den Weg des Stickstoffs zwischen der Atmosph&auml;re, dem pflanzlichen/tierischen Eiwei&szlig; und den Zwischenstufen Ammoniak, Ammonium, Nitrit und Nitrat. </p>
	    
	    

	    <p>Stickstoff kommt in jedem lebenden Organismus vor (Tiere, Pflanzen, Pilze, Bakterien), da Stickstoff ein wichtiger Baustoff f&uuml;r Aminos&auml;uren und der DNA/RNA ist. Jedoch k&ouml;nnen die allermeisten Lebewesen den elementaren Stickstoff nicht einfach &uuml;ber die Luft aufnehmen. Eine Ausnahme sind zum Beispiel Cyanobakterien. Markl Biologie (Oberstufe) schreibt hierzu: "Stickstoff geh&ouml;rt zu den wichtigsten Bauelementen biologischer Molek&uuml;le - denken Sie nur an die Aminos&auml;uren (und damit auch an die Proteine) oder die DNA, den Tr&auml;ger der Erbinformation. Da Luft zu 78% aus Stickstoff besteht, d&uuml;rfte daran eigentlich kein Mangel bestehen. Trotzdem geh&ouml;rt Stickstoff oft zu den Minimumfaktoren, die das Pflanzenwachstum begrenzen. Den chemisch &auml;u&szlig;erst stabilen Stickstoff (N<sub>2</sub>) aus der Luft k&ouml;nnen n&auml;mlich nur wenige Bakterienarten verarbeiten." (S. 351)</p>
	    
