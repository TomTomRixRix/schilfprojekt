	    <p>In Gew&auml;ssern sinken die organischen Substanzen (pflanzliche und tierische sterbliche &Uuml;berreste) zun&auml;chst auf den Grund. Hier werden diese Substanzen mit gebundenem Stickstoff von stickstoffbindenden Bakterien (Destruenten) zu Ammonium-Ionen abgebaut. Diesen Prozess nennt man Ammonifikation und ist in der Abbildung 2 der erste Schritt.</p>
	    <p>Bei der Nitrifikation (Schritt zwei in der Abbildung), die durch spezielle aerobe Bakterien durchgef&uuml;hrt wird, wird das Ammonium unter aeroben Bedingungen zun&auml;chst zu Nitrit, dann zu Nitrat oxidiert. Markl schreibt hierzu: "Im mehrstufigen Prozess der Nitrifikation wird Ammonium zu Nitrat (NO<sub>3</sub><sup>-</sup>) oxidiert, das die Pflanzen dann &uuml;ber die Wurzeln aus dem Boden aufnehmen. Die N<sub>2</sub>-Fixierer unter den Bakterien schleusen in gro&szlig;em Stil Stickstoff in die biologischen Kreisl&auml;ufe[...]." (S.352)</p>
	    <p>Das Nitrat kann von Pflanzen als N&auml;hrstoff aufgenommen werden; auch Assimilation genannt. Hierbei wird Stickstoff fixiert. (Cyanobakterien k&ouml;nnen sogar  atmosph&auml;rischen Stickstoff aufnehmen, sodass er im Organismus gebunden wird. Dies nennt man Stickstofffixierung (Schritt f&uuml;nf)). Sobald die Pflanzen sterben und zu Boden sinken, beginnt der Kreislauf grunds&auml;tzlich von neuem. Jedoch wird Nitrat auch durch Bakterien &uuml;ber Distickstoffmonooxid in elementaren Stickstoff reduziert. Dies geschieht bei Sauerstoffknappheit und nennt sich Denitrifikation (Schritt drei). </p>
	    <p>Bei der Nitratammonifikation (Schritt vier) wird Nitrat in Ammonium zur&uuml;ckreduziert. </p>
