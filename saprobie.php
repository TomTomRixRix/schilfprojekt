		<p>Das <b>Saprobiensystem</b> ist ein System zur Ermittlung des biologischen Verschmutzungsgrades (Saprobie) von Flie&szlig;gew&auml;ssern und Einordnung in Gew&auml;sserg&uuml;teklassen anhand des Saprobienindex. Dazu nutzt man die im Gew&auml;sser aufgefundenen Saprobier (verschiedene Arten von Protozoen, Kleinkrebsen und Insektenlarven) als Bioindikatoren.</p>
		<p>Mit dem Saprobiensystem wird die Belastung eines Flie&szlig;gew&auml;ssers mit organischen, leicht abbaubaren, sauerstoffzehrenden Substanzen gemessen. Andere Gew&auml;sserbelastungen werden damit nicht indiziert (z. B. Belastungen mit toxischen Stoffen: Schwermetalle, Pestizide, Belastung mit N&auml;hrsalzen...)<a href="#1" class="hoch">1</a>.</p>
		<p>Das Saprobiensystem ist ausschlie&szlig;lich auf Flie&szlig;gew&auml;sser anwendbar, in denen der Ablauf der Biomasse abbauenden heterotrophen Prozesse im Zuge der Selbstreinigung r&auml;umlich getrennt in Flie&szlig;richtung verl&auml;uft. In stehenden Gew&auml;ssern finden diese Vorg&auml;nge gleichzeitig am gleichen Ort statt, so dass es keine Leitorganismen f&uuml;r die Saprobie geben kann. Stehende Gew&auml;sser sind nur durch ihren Trophiezustand charakterisierbar, d.h. das Ausma&szlig; der Biomasse aufbauenden, autotrophen Prozesse.<a href="#2" class="hoch">2</a> </p>
		<p>Auf die Wakenitz bezogen k&ouml;nnte man im oberen, nat&uuml;rlicheren Flussverlauf eine Anwendbarkeit des Saprobiensystems erwarten, w&auml;hrend in Stadtn&auml;he die Wakenitz seenartig erweitert ist.</p>
		
		<p><b>Ben&ouml;tigte Ger&auml;te zum Sammeln der Organismen</b></p>
		
		<ul>
