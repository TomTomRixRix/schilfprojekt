        </ul>
        
		<p><b>Durchf&uuml;hrung der Freilandarbeit</b></p>
		<p>Steine oder Holzst&uuml;cke des Gew&auml;ssers vorsichtig aus dem Wasser nehmen und besonders an der Unterseite nach Organismen absuchen.</p>
		<p>Gefundene Organismen nach Arten trennen und in unterschiedliche Gef&auml;&szlig;e &uuml;berf&uuml;hren und bestimmen.</p>
		<p>Schlammiges oder sandiges Substrat mit dem K&uuml;chensieb aufnehmen und langsam im freien Wasser durchsp&uuml;len (Siebrand dabei &uuml;ber Wasseroberfl&auml;che), Organismen vom Siebgitter in die wei&szlig;e Schale abklopfen oder absammeln, anschlie&szlig;end nach Arten trennen und bestimmen. Nach den Untersuchungen sollten die Organismen m&ouml;glichst wieder dem Gew&auml;sser zugef&uuml;hrt werden.</p>
		<p>Die H&auml;ufigkeit von Indikatororganismen, denen ein Saprobienwert zugeordnet werden kann (Tabelle)  sollte grob abgesch&auml;tzt werden: vereinzelt, h&auml;ufig, massenhaft. </p>
		<br>
		<p>Um den Saprobienindex zu berechnen, multipliziert man die Anzahl der gefundenen Lebewesen einer Art (bzw. die grobe H&auml;ufigkeit) mit dem dazu geh&ouml;rigen Saprobienwert der Indikatororganismen. Dies wiederholt man mit jeder Art und addiert die Produkte.</p>
		<br>
		<p>Teilt man nun die Gesamtsumme durch die gesamte Anzahl gefundener Indikatorlebewesen, erh&auml;lt man den Saprobienindex. </p>
        
        <h2 style="text-align:left; ">Chemische Gew&auml;sserg&uuml;teanalyse </h2>
        
        <p>Die chemische Gew&auml;sserg&uuml;te (in vier G&uuml;teklassen unterteilt) bewertet ein Flie&szlig;gew&auml;sser anhand seiner Belastung mit N&auml;hrsalzen wie Nitrat oder Phosphor sowie anhand der Konzentration von Schwermetallen wie Cadmium, Chrom, Kupfer, Quecksilber, Nickel, Blei oder Zink in den Sedimenten. Es ist mit Hilfe der nebenstehenden Tabelle eine stoffbezogene G&uuml;tezuordnung m&ouml;glich.</p>
        <p>Dem Gew&auml;sser wird eine Wasserprobe entnommen und die darin enthaltenen Stoffe gemessen, z.B.: Ammonium, Phosphat, Chlorid, Sulfat, Sauerstoff. Diese Werte vergleicht man nun mit einer Tabelle, die eine Zuordnung der G&uuml;teklassen zu bestimmten Stoffkonzentrationen enth&auml;lt.</p>
        
        <p><b>Die <i>BACH</i>-Methode zur Gesamtbewertung der chemische Gew&auml;sserg&uuml;te</b> <a href="#3" class="hoch">3</a></p>
        
        <p>Geruch und F&auml;rbung geben bereits Hinweise auf die G&uuml;te: Unter aufgenommenen Steinen in Gew&auml;ssern der G&uuml;teklasse I (unbelastetes oder gering belastetes Wasser) ist keine Bildung von schwarzen Flecken erkennbar. Im Gegensatz dazu findet man bei Gew&auml;ssern der G&uuml;teklasse II (m&auml;&szlig;ig belastetes Wasser) an einigen ruhigen Stellen Steine mit engbegrenzten schwarzen Flecken bzw. schwarz gef&auml;rbtes Sediment in den tieferen Schichten des Baches. Unter fast allen Steinen sind in Gew&auml;ssern der G&uuml;teklasse III (stark verschmutzt) gro&szlig;e schwarze Flecken zu erkennen. Die Bodenschichten sind deutlich schwarz gef&auml;rbt, die o.g. Faulschlammablagerungen sind erkennbar. Beim Aufw&uuml;hlen des Untergrundes kann man den typischen Geruch von Schwefelwasserstoff nach faulen Eiern erkennen. </p>
        <p><u>Erfassung der Parameter</u></p>
        <p>Das Wasser f&uuml;r die Untersuchung der Proben sollte grunds&auml;tzlich aus der Mitte des Gew&auml;ssers entnommen werden, da am Rand z.B. durch geringere Flie&szlig;geschwindigkeiten und die Ablagerung von Sedimenten h&auml;ufig verf&auml;lschte Messergebnisse erzielt werden. F&uuml;r die Wasserentnahme sind verschlie&szlig;bare Plastikflaschen zu verwenden. Die Beh&auml;lter, in denen eine Probe transportiert werden soll, f&uuml;llt man randvoll. </p>
        <p><u>Bewertung der Messergebnisse nach BACH</u></p>
        <p>F&uuml;r die Auswertung kann die Methode von BACH genutzt werden (im Jahr 1980 f&uuml;r den Einsatz in Beh&ouml;rden konzipiert). Zu einer Gesamt-Bewertung der Gew&auml;sserg&uuml;te gelangt man mit dieser Methode durch die Berechnung eines Chemischen Index' (Ci) aus 8 relativ einfach messbaren Parametern. Diese werden zun&auml;chst entsprechend ihrer relativen &ouml;kologischen Bedeutung f&uuml;r das Gew&auml;sser mit einer Wichtungszahl (W) bewertet (z.B. Sauerstoff 0.2, pH-Wert 0.1). Den Messergebnissen werden mittels vorliegender Tabellen Indexzahlen (I) zwischen 1 und 100 zugeordnet, welche mit den Wichtungen potenziert und sodann miteinander multipliziert werden: </p>
        <p>C<sub>i</sub> = I<sub>1</sub><sup>W1</sup> x I<sub>2</sub><sup>W2</sup> x.... x I<sub>8</sub><sup>W8</sup></p>
        <p>Berechnung des Beispiels:</p>
        <p>C<sub>i</sub> = 97,25<sup>08</sup> x 51,8<sup>0,2</sup> x 68<sup>0,2</sup> x 94<sup>0,1</sup> x 85<sup>0,1</sup> x 72<sup>0,1</sup> x 60<sup>0,15</sup> x 12,92<sup>0,07</sup>
        <p>C<sub>i</sub> = 61,3 </p>
        
        <p>Das Gesamtergebnis wird entsprechend der Skala (rechts) einer Gew&auml;sserg&uuml;teklasse zugeordnet.</p>
        <p>Der obige Beispiel-Wert entspr&auml;che also der Gew&auml;sserg&uuml;teklasse II (m&auml;&szlig;ig belastet). </p>
        <p>Eine Vereinfachung der Methode ist m&ouml;glich, indem nur sechs der genannten Parameter (ohne Phosphat und Ammonium) gemessen und die Berechnung des Indexwertes wie folgt vereinfacht wird: </p>
        <p>C<sub>i</sub>= I<sub>1</sub> x W <sub>1</sub> + I<sub>2</sub> x W<sub>2</sub> +.... + I<sub>6</sub> x W<sub>6</sub>. </p>



