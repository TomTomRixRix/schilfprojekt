<p>Stickstoff kommt in jedem lebenden Organismus vor (Tiere, Pflanzen, Pilze, Bakterien), da Stickstoff ein wichtiger Baustoff f&uuml;r Aminos&auml;uren und der DNA/RNA ist. Jedoch k&ouml;nnen die allermeisten Lebewesen den elementaren Stickstoff nicht einfach &uuml;ber die Luft aufnehmen. Eine Ausnahme sind zum Beispiel Cyanobakterien. </p>
<p>In Gew&auml;ssern sinken die organischen Substanzen (pflanzliche und tierische sterbliche &uuml;berreste) zun&auml;chst auf den Grund. Hier werden diese Substanzen von stickstoffbindenden Bakterien (Destruenten) zu Ammonium abgebaut. Diesen Prozess nennt man <b>Ammonifikation</b>. </p>
<p>Bei der <b>Nitrifikation</b>, die durch spezielle aerobe Bakterien durchgef&uuml;hrt wird, wird das Ammonium unter aeroben Bedingungen zun&auml;chst zu Nitrit, dann zu Nitrat oxidiert. </p>
<p>Das Nitrat kann nun von Pflanzen als N&auml;hrstoff aufgenommen werden; auch <b>Assimilation</b> genannt. Sobald die Pflanzen sterben beginnt der Kreislauf von neuem. </p>
<p>Bei der <b>Denitrifizierung/Stickstoffizierung</b> unter anaeroben Bedingungen werden Nitrit und Nitrat durch denitrifizierende Bakterien zu Stickstoff reduziert. Dieser Stickstoff entweicht in die Atmosph&auml;re.</p>


