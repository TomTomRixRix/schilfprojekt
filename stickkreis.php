	<h1 align="center">Stickstoffkreislauf </h1>
<p>Der Stickstoffkreislauf beschreibt den Weg des Stickstoffs zwischen der Atmosph&auml;re, dem pflanzlichen/tierischen Eiwei&szlig; und den Zwischenstufen Ammoniak, Ammonium, Nitrit und Nitrat. </p>

