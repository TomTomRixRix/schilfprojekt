		<b>Diskussion</b>
        <p>Nach den derzeitigen Untersuchungen dominiert der Genotyp "A1 / A2, Cp-I, Cp-A". Er wurde in allen Proben vom Kleinen See und von Eichholz gefunden, sowie in wenigen Proben aus Absalonshorst. Sollte dieser Genotyp anf&auml;llig sein f&uuml;r ver&auml;nderte Umweltbedingungen, k&ouml;nnte dies den starken R&uuml;ckgang der Schilfbest&auml;nde am Kleinen See und in Eichholz erkl&auml;ren, sowie das intermedi&auml;re Wachstum am Misch-Standort Absalonshorst.</p>
    <p>In allen acht Proben des gut w&uuml;chsigen Standorts Wallbrechtbr&uuml;cke wurden bisher ebenfalls die Allele A1 / A2 und das Allel Cp-I nachgewiesen; der Chloroplasten-marker Cp-A / Cp-B wurde bisher noch nicht getestet. Sollte sich auch hier ausschliesslich das Allel Cp-A finden, kann der Genotyp "A1 / A2, Cp-I, Cp-A" nicht allein verantwortlich sein f&uuml;r den Schilfr&uuml;ckgang an den Standorten Kleiner See und Eichholz. Da die vorliegende Analyse, mit derzeit drei untersuchten Markern, jedoch sehr punktuell ist, kann nicht ausgeschlossen werden, dass sich der Genotyp am Standort Wallbrechtbr&uuml;cke von den Genotypen Kleiner See und Eichholz unterscheidet, die Unterschiede bisher lediglich nicht erfa&szlig;t wurden.</p>
    <p>Klar ist hingegen der genetische Unterschied zum Standort Gro&szlig; Sarau. Dort hatten alle 11 untersuchten Proben den abweichenden Genotyp "B1, Cp-II, Cp-B". Dieser Genotyp wurde bisher an keinem der anderen Standorte gefunden. Somit k&ouml;nnte dieser Genotyp mit einem widerstandsf&auml;higen Ph&auml;notyp einhergehen, der das gute Schilfwachstum in Gro&szlig;-Sarau erm&ouml;glicht.</p>
    <br>
    <b>Ausblick</b>
    <p>Vervollst&auml;ndigung der genetischen Analyse der Proben vom Standort Wallbrechtbr&uuml;cke.</p>
    <p>Ob der in Gro&szlig; Sarau nachgewiesene Genotyp urs&auml;chlich f&uuml;r das gute Schilfwachstum an diesem Standort verantwortlich ist, m&uuml;ssten Verpflanzungsexperimente dieser Rhizome an einen anderen Standort zeigen. In Gro&szlig; Sarau entnommene Rhizome befinden sich derzeit unter kontrollierten Zuchtbedingungen und zeigen den Sarau-typischen Genotyp. Dieser spezielle Genotyp ist mit Hilfe der oben beschriebenen Techniken auch nach einer Verpflanzung jederzeit wieder auffindbar, sofern sich die Pflanzen rein vegetativ vermehren.</p>

