    <b>Ausgangssituation</b>

    <p>Die untersuchten Schilfstandorte an der Wakenitz zeigen im Verlauf der letzten Jahre ein unterschiedliches Wachstumsverhalten. W&auml;hrend die Best&auml;nde im Kleinen See und in Eichholz stark r&uuml;ckl&auml;ufig sind, entwickeln sich die Best&auml;nde in Gro&szlig; Sarau und an der Wallbrechtbr&uuml;cke (Beprobung seit 2013) gut. Der k&uuml;nstlich angelegte Schilfbestand in Absalonshorst zeigt ein intermedi&auml;res Wachstum. </p>
    <p>Bisher (Stand: 2013) gibt es keinerlei Hinweise darauf, dass dieses unterschiedliche Wachstumsverhalten eindeutig mit dem Auftreten verschiedener biotischer oder abiotischer Faktoren zusammen h&auml;ngt.</p>
    <p>Es ist also denkbar, dass dieses unterschiedliche Wachstumsverhalten das Auftreten unterschiedlicher Genotypen wiederspiegelt. D.h. auf &auml;hnliche Umweltbedingungen reagieren die Best&auml;nde, aufgrund ihrer unterschiedlichen genetischen Konstitution, verschieden. Diese Vermutung wird durch die besondere Vermehrungsweise des Schilfrohrs untermauert. </p>
    <p>Schilf kann sich sowohl generativ (&uuml;ber Samen) als auch vegetativ (&uuml;ber Rhizomsprosse) vermehren. Das kann zu verschiedenen genetischen Szenarien f&uuml;hren (s. Abb. rechts): </p>
    <ul>
        <li> Ein Schilfbestand kann aus vielen genetisch unterschiedlichen Pflanzen bestehen</li>
        <li> Ein Schilfbestand kann im Extremfall aus den Nachkommen einer einzelnen Pflanze bestehen, stellt somit einen genetisch identischen Klon dar </li>
        <li> Ein Schilfbestand kann ein Mosaik verschiedener Klone sein. </li>
    </ul>
    <p>Klonales Wachstum kann sehr vorteilhaft sein: Unter g&uuml;nstigen Umweltbedingungen k&ouml;nnen schnell gro&szlig;e Fl&auml;chen besiedelt werden, ohne die Ausbildung von Bl&uuml;ten und Samen. Auf der anderen Seite kann klonales Wachstum bei ge&auml;nderten Umweltbedingungen fatale Folgen haben: Ist der vorliegende Genotyp nur unzureichend angepasst, ist davon der gesamte Bestand betroffen.</p>
    <br>
    
    <b>Methoden</b>
    
    <p>Zur Testung der Schilfgenotypen wurde an den verschiedenen Standorten junges Pflanzenmaterial gesammelt und jeweils ein genetischer Fingerabdruck angefertigt. Diese Technik entspricht dem genetischen Fingerabdruck, wie er in der Forensik angewendet wird. Mit dieser Technik werden genetische Unterschiede zwischen verschiedenen Individuen einer Art analysiert. Aussagekr&auml;ftige Unterschiede findet man innerartlich nur in nicht-kodierenden Bereichen der DNA. Das k&ouml;nnen sowohl Introns als auch intergenische Sequenzabschnitte sein. Da Mutationen in diesen DNA-Bereichen keinerlei Konsequenzen f&uuml;r den Ph&auml;notyp haben, gibt es keinen Selektionsdruck gegen diese Sequenzver&auml;nderungen, sodass sie sich in diesen nicht-kodierenden Bereichen anh&auml;ufen. </p>
    <p>In diesem Schilfprojekt werden solche Unterschiede mittels Polymerase-Kettenreaktion (PCR) und anschlie&szlig;ender Gel-Elektrophorese analysiert. Diese Technik ist nur dann aussagekr&auml;ftig, wenn die PCR-Produkte unterschiedliche Gr&ouml;&szlig;en aufweisen; die Mutationen also zu einem Verlust oder einem Zugewinn an DNA-Sequenz  gef&uuml;hrt haben. Solche Ver&auml;nderungen treten geh&auml;uft im Bereich von Mikrosatelliten auf, also Clustern aus kurzen, vielfach wiederholten DNA-Sequenzen. Flankieren die beiden PCR-Primer ein solches Cluster, kann dessen variable Gr&ouml;sse bestimmt werden (s.u.).</p>
