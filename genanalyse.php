    <u>DNA-Isolation</u>
    <p>Das gesammelte junge Pflanzenmaterial wurde bis zur Bearbeitung bei -80 &deg;C gelagert. Zur DNA-Isolation wurde das Pflanzen-DNA-Isolations-Kit der Firma Qiagen verwendet (www.qiagen.com). Dieser Kit kam erstmals in der LOLA-Summerschool 2010 zum Einsatz. Im Vorfeld wurde die Herstelleranleitung von Herrn PD Dr. C.L. Schmidt auf das Silikat-haltige Schilfmaterial adaptiert, sodass die Versuchsanleitung final 33 Arbeitsschritte umfa&szlig;te, statt der urspr&uuml;nglichen 12 Arbeitsschritte.</p>
    <br>
