    	<b>Ergebnisse</b>
        <p>Die Schilfbest&auml;nde an den Standorten Eichholz und Kleiner See erscheinen bisher genetisch gleich und in sich homogen. Kern-lokalisiert wurden die Allele A1 und A2 nachgewiesen, Chloroplasten-lokalisiert die Allele Cp-I und Cp-A. Auch am Standort Wallbrechtbr&uuml;cke, der seit 2013 beprobt wird, wurde bisher ausschlie&szlig;lich dieser Genotyp gefunden; allerdings wurden dort bisher erst zwei Marker getestet (es fehlt noch Cp-A / Cp-B).</p>
        <p>Dieser Genotyp (A1 / A2, Cp-I, Cp-A) wurde auch am Standort Absalonshorst gefunden, jedoch zus&auml;tzlich zwei weitere Kern-lokalisierte Allel-Kombinationen ( C1 / C2 und C1 / C5). Diese Genotypen tragen im Chloroplasten wiederum die Allele Cp-I und Cp-A. Die Heterogenit&auml;t der Genotypen in Absalonshorst ist m&ouml;glicherweise die Folge der k&uuml;nstlichen Schilf-Anpflanzung an diesem Standort.</p>
        <p>Genetisch gut zu unterscheiden von den vier oben beschriebenen Best&auml;nden sind die analysierten Schilfproben vom Standort Gro&szlig; Sarau. Die 12 untersuchten Proben sind untereinander homogen und klar zu unterscheiden von den beschriebenen Genotypen an den anderen Standorten. Der Genotyp "Gro&szlig; Sarau" ist in den Abbildungen blau markiert und beinhaltet das Kern-lokalisierte Allel B1 (homozygot) und die Chloroplasten-lokalisierten Allele Cp-II und Cp-B.</p>
